function a=rk4_1(f,t,x,y,h)
X=zeros(5,1);
Y=zeros(5,1);
T=zeros(5,1);
K=zeros(5,1);
X(1)=x;
Y(1)=y;
T(1)=t;
K(1)=f(X(1),Y(1),T(1));
X(2)=x+Y(1)*h/2;
Y(2)=y+K(1)*h/2;
T(2)=t+h/2;
K(2)=f(X(2),Y(2),T(2));
X(3)=x+Y(2)*h/2;
Y(3)=y+K(2)*h/2;
K(3)=f(X(3),Y(3),T(2));
X(4)=x+Y(3)*h;
Y(4)=y+K(3)*h;
K(4)=f(X(4),Y(4),T(2)+h/2);
X(5)=h/6*(Y(1)+2*Y(2)+2*Y(3)+Y(4));
x1=x+X(5);
Y(5)=h/6*(K(1)+2*K(2)+2*K(3)+K(4));
y1=y+Y(5);
a=[x1 y1];